export function heroName(id: string | null) {
  if (!id) return "-";
  const n = Number(id);
  return HEROES[n] ?? `Hero(${id})`;
}

export function itemName(id: number) {
  return ITEMS[id] ?? `Item(${id})`;
}

export function fmtTime(seconds: number) {
  const s = Math.max(0, Math.floor(seconds));
  const m = Math.floor(s / 60);
  const r = s % 60;
  return `${m}:${String(r).padStart(2, "0")}`;
}

export function hasItem(id: number) {
  return Object.prototype.hasOwnProperty.call(ITEMS, id);
}
