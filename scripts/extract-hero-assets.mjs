import fs from "node:fs";
import path from "node:path";

const cwd = process.cwd();
const inputArg = process.argv[2];
const outputArg = process.argv[3];

if (!inputArg) {
  console.error("Usage: npm run extract:hero-assets -- <inputHeroData.json> [outputFile]");
  process.exit(1);
}

const inputFile = path.resolve(cwd, inputArg);
const outputFile = path.resolve(cwd, outputArg ?? "lib/heroAssets.generated.ts");

if (!fs.existsSync(inputFile)) {
  console.error(`Input file not found: ${inputFile}`);
  process.exit(1);
}

const raw = fs.readFileSync(inputFile, "utf8");
const parsed = JSON.parse(raw);

if (!Array.isArray(parsed)) {
  console.error("Expected hero_data JSON to be an array.");
  process.exit(1);
}

const IMAGE_FIELDS = [
  "icon_image_small",
  "icon_hero_card",
  "minimap_image",
  "name_image",
  "top_bar_vertical_image",
  "background_image",
  "hero_card_critical",
  "hero_card_gloat",
  "weapon_image",
];

function parsePanoramaAsset(value) {
  if (typeof value !== "string") return null;
  const match = value.match(/^panorama:\"file:\/\/{images}\/(.+)\"$/);
  if (!match) return null;
  return match[1];
}

function normalizeWebPath(assetPath) {
  if (!assetPath) return null;

  if (assetPath.endsWith(".svg")) {
    return `/assets/${assetPath}`;
  }

  if (assetPath.endsWith(".psd")) {
    return `/assets/${assetPath.replace(/\.psd$/i, ".png")}`;
  }

  return `/assets/${assetPath}`;
}

const heroes = parsed
  .map((hero) => {
    const id = Number(hero?.id);
    if (!Number.isFinite(id)) return null;

    const iconFields = {};

    for (const field of IMAGE_FIELDS) {
      const rawValue = hero?.[field] ?? null;
      const assetPath = parsePanoramaAsset(rawValue);
      iconFields[field] = {
        raw: rawValue,
        assetPath,
        webPath: normalizeWebPath(assetPath),
      };
    }

    return {
      id,
      className: hero?.class_name ?? null,
      heroType: hero?.hero_type ?? null,
      iconFields,
    };
  })
  .filter(Boolean)
  .sort((a, b) => a.id - b.id);

const output = `/* eslint-disable */
// Auto-generated by scripts/extract-hero-assets.mjs
// Source: ${inputFile.replace(/\\/g, "/")}
// Generated at: ${new Date().toISOString()}

export type HeroImageField = {
  raw: string | null;
  assetPath: string | null;
  webPath: string | null;
};

export type HeroAssetEntry = {
  id: number;
  className: string | null;
  heroType: string | null;
  iconFields: Record<string, HeroImageField>;
};

export const HERO_ASSETS: HeroAssetEntry[] = ${JSON.stringify(heroes, null, 2)};

export const HERO_ASSETS_BY_ID: Record<number, HeroAssetEntry> = Object.fromEntries(
  HERO_ASSETS.map((entry) => [entry.id, entry])
) as Record<number, HeroAssetEntry>;
`;

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(outputFile, output, "utf8");

console.log(`Wrote ${heroes.length} heroes to ${outputFile}`);
